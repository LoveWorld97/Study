void WriteCmd(unsigned char I2C_Command) //写命令
{
    HAL_I2C_Mem_Write(&hi2c1, OLED0561_ADD, COM, I2C_MEMADD_SIZE_8BIT, &I2C_Command, 1, 100);
}

void WriteDat(unsigned char I2C_Data) //写数据
{
    HAL_I2C_Mem_Write(&hi2c1, OLED0561_ADD, DAT, I2C_MEMADD_SIZE_8BIT, &I2C_Data, 1, 100);
}
void OLED_Init(void)
{
    HAL_Delay(100); //这里的延时很重要

    WriteCmd(0xAE); //display off
    WriteCmd(0x20); //Set Memory Addressing Mode
    WriteCmd(0x10); //00,Horizontal Addressing Mode;01,Vertical Addressing Mode;10,Page Addressing Mode (RESET);11,Invalid
    WriteCmd(0xb0); //Set Page Start Address for Page Addressing Mode,0-7
    WriteCmd(0xc8); //Set COM Output Scan Direction
    WriteCmd(0x00); //---set low column address
    WriteCmd(0x10); //---set high column address
    WriteCmd(0x40); //--set start line address
    WriteCmd(0x81); //--set contrast control register
    WriteCmd(0xff); //亮度调节 0x00~0xff
    WriteCmd(0xa1); //--set segment re-map 0 to 127
    WriteCmd(0xa6); //--set normal display
    WriteCmd(0xa8); //--set multiplex ratio(1 to 64)
    WriteCmd(0x3F); //
    WriteCmd(0xa4); //0xa4,Output follows RAM content;0xa5,Output ignores RAM content
    WriteCmd(0xd3); //-set display offset
    WriteCmd(0x00); //-not offset
    WriteCmd(0xd5); //--set display clock divide ratio/oscillator frequency
    WriteCmd(0xf0); //--set divide ratio
    WriteCmd(0xd9); //--set pre-charge period
    WriteCmd(0x22); //
    WriteCmd(0xda); //--set com pins hardware configuration
    WriteCmd(0x12);
    WriteCmd(0xdb); //--set vcomh
    WriteCmd(0x20); //0x20,0.77xVcc
    WriteCmd(0x8d); //--set DC-DC enable
    WriteCmd(0x14); //
    WriteCmd(0xaf); //--turn on oled panel
}

void OLED_SetPos(unsigned char line, unsigned char column) //设置起始点坐标
{
    WriteCmd(0xb0 + line);                   /*OLED设置大行数8，大行0-7，每大行由8小行组成*/
    WriteCmd(((column & 0xf0) >> 4) | 0x10); /*设置列地址高位*/
    WriteCmd((column & 0x0f) | 0x00);        /*设置列地址低位*/
}

void OLED_Fill(unsigned char fill_Data) //全屏填充
{
    unsigned char m, n;
    for (m = 0; m < 8; m++)
    {
        WriteCmd(0xb0 + m); //page0-page1
        WriteCmd(0x00);     //low column start address
        WriteCmd(0x10);     //high column start address
        for (n = 0; n < 128; n++)
        {
            WriteDat(fill_Data);
        }
    }
}

void OLED_CLS(void) //清屏
{
    OLED_Fill(0x00);
}

void OLED_ON(void)
{
    WriteCmd(0X8D); //设置电荷泵
    WriteCmd(0X14); //开启电荷泵
    WriteCmd(0XAF); //OLED唤醒
}

void OLED_OFF(void)
{
    WriteCmd(0X8D); //设置电荷泵
    WriteCmd(0X10); //关闭电荷泵
    WriteCmd(0XAE); //OLED休眠
}

void show_char_a_type(uint8_t line, uint8_t column, char *array)
{
    uint8_t i = 0;

    OLED_SetPos(line, column);
    for (i = 0; i < 8; i++)
    {
        WriteDat(array[i]);
    }
    for (i = 0; i < 8; i++)
    {
        WriteDat(array[i + 8]);
    }
}

void show_char_a1type(uint8_t line, uint8_t column, char character)
{
    uint8_t i = 0;

    for (i = 0; i < ASC_16_num; i++)
    {
        if (ASC_16[i].Index[0] == character)
            break;
    }

    if (i == ASC_16_num)
        return;

    show_char_a_type(line, column, ASC_16[i].Msk);
}

void show_string_atype(uint8_t line, uint8_t column, char *data, uint8_t len)
{
    uint8_t i = 0;

    for (i = 0; i < len; i++)
    {
        show_char_a1type(line, column, *data);
        column += 1;
        data += 1;
    }
}

void show_hz(uint8_t line, uint8_t column, char *array)
{
    uint8_t i = 0;

    OLED_SetPos(line, column);
    for (i = 0; i < 16; i++)
    {
        WriteDat(array[i]);
    }
    OLED_SetPos(line, column + 1);
    for (i = 0; i < 16; i++)
    {
        WriteDat(array[i + 16]);
    }
}
/*
*****************************************************************
功能描述：显示一个汉字
函数属性：外部调用
说明事项：调用show_hz()函数

入口参数：lin:行(0-7), column: 列(0-7)  array: 字模区数组   black:是否反白显示标志(0:正常显示，非0：反白显示)
出口参数：void

应用实例：show_hanzi(2, 0, "你");         //显示一个汉字"你"  宽x高=16x16
*****************************************************************
*/
void show_hanzi(uint8_t line, uint8_t column, char *data)
{
    unsigned int i;

    for (i = 0; i < GB_16_num; i++)
    {
        if (GB_16[i].Index[0] == data[0] && GB_16[i].Index[1] == data[1] && GB_16[i].Index[2] == data[2] && GB_16[i].Index[3] == data[3])
        {
            break;
        }
    }

    if (i == GB_16_num)
    {
        return;
    }
    show_hz(line, column, GB_16[i].Msk);
}

/*
*****************************************************************
功能描述：显示一串中文简体输出
函数属性：外部调用
说明事项：查表显示输出        汉字输出个数num: num < 8 - column

入口参数：lin:行(0-7), column: 列(0-7)  array: 字模区数组	black:是否反白显示标志(0:正常显示，非0：反白显示)
出口参数：void

*****************************************************************
*/

void show_chs_string(uint8_t line, uint8_t column, char *data)
{
    while (*data != '\0')
    {
        show_hanzi(line, column, data);
        column += 4;
        data += 4; /*UTF-8,一个汉字GB码由4个字节组成*/
    }
}

/* Parameters     : x0,y0 -- 起始点坐标(x0:0~127, y0:0~7); x1,y1 -- 起点对角线(结束点)的坐标(x1:1~128,y1:1~8)
Description    : 显示BMP位图
*/
void show_drawBMP(unsigned char x0, unsigned char y0, unsigned char x1, unsigned char y1, unsigned char BMP[])
{
    unsigned int j = 0;
    unsigned char x, y;

    if (y1 % 8 == 0)
        y = y1 / 8;
    else
        y = y1 / 8 + 1;
    for (y = y0; y < y1; y++)
    {
        OLED_SetPos(x0, y);
        for (x = x0; x < x1; x++)
        {
            WriteDat(BMP[j++]);
        }
    }
}

u32 oled_pow(u8 m, u8 n)
{
    u32 result = 1;
    while (n--)
        result *= m;
    return result;
}
